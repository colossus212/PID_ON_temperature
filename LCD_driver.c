/********************************************************************************************
	 DDA称为数值微分画线算法，是直线生成算法中最简单的一种。原理相当简单，就是最直观的根据斜率的
	 偏移程度，决定是以x为步进方向还是以y为步进方向。然后在相应的步进方向上，步进变量每次增加一个
	 像素，而另一个相关坐标变量则为Yk_1=Yk+m（以x为步进变量为例，m为斜率）
 ********************************************************************************************/

void LCDLineDDA(int x0,int y0,int x1,int y1)
{
	float dy,dx,x,y,m;
	LCD_CS=0;
    dx=x1-x0;
    dy=y1-y0;
    m=dy/dx;
    if(x0<x1)
    {
        if(m<=1&&m>=-1)
        {
              y=y0;
              for(x=x0;x<=x1;x++)
              {
                   Address_setLCD(x,y+0.5,x+1,y+1+0.5);
	               LCD_Write_DATA(0,255);
                  y+=m;
              }
        }
    }
    if(x0>x1)
    {
        if(m<=1&&m>=-1)
        {
              y=y0;
              for(x=x0;x>=x1;x--)
              {
               	   Address_setLCD(x,y+0.5,x+1,y+1+0.5);
	               LCD_Write_DATA(0,255);
                  y-=m;
              }
        }
    }
    if(y0<y1)
    {
        if(m>=1||m<=-1)
        {
              m=1/m;
              x=x0;
              for(y=y0;y<=y1;y++)
              {
              
				 Address_setLCD(x+0.5,y,x+1+0.5,y+1);
	               LCD_Write_DATA(0,255);
                  x+=m;
              }
        }
    }
    if(y0>y1)
    {
        if(m<=-1||m>=1)
        {
              m=1/m;
              x=x0;
              for(y=y0;y>=y1;y--)
              {
             
				   Address_setLCD(x+0.5,y,x+1+0.5,y+1);
	               LCD_Write_DATA(0,255);
                  x-=m;
              }
        }
    }
	LCD_CS=1;
}

void Address_setLCD(unsigned int x1,unsigned int y1,unsigned int x2,unsigned int y2)
{  
    LCD_Write_COM(0x00,0x20);LCD_Write_DATA(x1>>8,x1);	  //设置X坐标位置
    LCD_Write_COM(0x00,0x21);LCD_Write_DATA(y1>>8,y1);   //设置Y坐标位置

								 

    LCD_Write_COM(0x00,0x37);LCD_Write_DATA(x1>>8,x1);   //开始X
	LCD_Write_COM(0x00,0x39);LCD_Write_DATA(y1>>8,y1);	  //开始Y
    LCD_Write_COM(0x00,0x36);LCD_Write_DATA(x2>>8,x2);   //结束X
	LCD_Write_COM(0x00,0x38);LCD_Write_DATA(y2>>8,y2);   //结束Y

	LCD_Write_COM(0x00,0x22);							 
}


